package com.practice.code.linkedlists;

import com.practice.code.model.ListNode;
import com.practice.code.runner.CodeRunner;

public class IntersectionOfTwoLinkedLists implements CodeRunner {
    @Override
    public void run() {

    }

// approach
//Set pointer pA to point at headA.
//Set pointer pB to point at headB.
//While pA and pB are not pointing at the same node:
//    If pA is pointing to a null, set pA to point to headB.
//    Else, set pA to point at pA.next.
//    If pB is pointing to a null, set pB to point to headA.
//    Else, set pB to point at pB.next.
//return the value pointed to by pA (or by pB; they're the same now)
    public ListNode getIntersectionNode(ListNode headA, ListNode headB) {
        ListNode nodeA = headA;
        ListNode nodeB = headB;
        while (nodeA != nodeB) {
            if (nodeA == null) {
                nodeA = headB;
            } else {
                nodeA = nodeA.next;
            }
            if (nodeB == null) {
                nodeB = headA;
            } else {
                nodeB = nodeB.next;
            }
//            original code below
//            nodeA = nodeA == null ? headB : nodeA.next;
//            nodeB = nodeB == null ? headA : nodeB.next;
        }
        return nodeA;
        // Note: In the case lists do not intersect, the pointers for A and B
        // will still line up in the 2nd iteration, just that here won't be
        // a common node down the list and both will reach their respective ends
        // at the same time. So nodeA will be NULL in that case.
    }
}
